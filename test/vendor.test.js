  // Plant a skills dir under BOTH a semver version and a hash dir.
  // The hash dir lexicographically sorts after "5.1.0" but before "10.0.0",
  // so the old sort().reverse() would have returned the hash dir.
  const base = join(home, SP_BASE);
  const hashDir = join(base, "6fd450765978", "skills");
