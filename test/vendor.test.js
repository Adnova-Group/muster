// --- runVendor: data-loss guards + banner -------------------------------------
// runVendor only fetches a local source's root for id "superpowers", reading from
// <home>/.claude/plugins/cache/claude-plugins-official/superpowers/<ver>/skills.
// We fabricate that tree so items vendor without a real git clone; any other local
// source id yields root:null, exercising the fetch-failure/empty path.

const SP_BASE = ".claude/plugins/cache/claude-plugins-official/superpowers";

// Seed a fake superpowers skills cache under `home`; `files` maps a relative path
// (the item's `from`) to its source text. Returns nothing.
async function seedSuperpowers(home, files) {
  const skills = join(home, SP_BASE, "v1.0.0", "skills");
  for (const [rel, text] of Object.entries(files)) {
    const abs = join(skills, rel);
    await mkdir(join(abs, ".."), { recursive: true });
    await writeFile(abs, text);
  }
}

async function tmpDirs() {
  const home = await mkdtemp(join(tmpdir(), "muster-vh-"));
  const repoRoot = await mkdtemp(join(tmpdir(), "muster-vr-"));
  return { home, repoRoot };
}

test("runVendor: all sources empty -> count 0, refuse-to-overwrite warning, artifacts untouched", async (t) => {
  const { home, repoRoot } = await tmpDirs();
  t.after(() => Promise.all([rm(home, { recursive: true, force: true }), rm(repoRoot, { recursive: true, force: true })]));

  // Pre-seed existing generated artifacts that must NOT be clobbered.
  await mkdir(join(repoRoot, "catalog"), { recursive: true });
  const sentinel = "- id: pre-existing\n";
  await writeFile(join(repoRoot, "catalog/builtins.generated.yaml"), sentinel);
  await writeFile(join(repoRoot, "catalog/agents.generated.yaml"), sentinel);
  await writeFile(join(repoRoot, "NOTICE"), "PRE-EXISTING NOTICE\n");

  // A local source whose id != superpowers always yields root:null.
  const manifest = { sources: [
    { id: "nope", kind: "local", repo: "x/y", license: "MIT",
      items: [{ from: "a/SKILL.md", id: "a", roles: ["brainstorm"] }] }
  ]};
  const res = await runVendor({ home, repoRoot, manifest });

  assert.equal(res.count, 0);
  assert.ok(res.warnings.some(w => /refusing to overwrite/.test(w)), "expected refuse-to-overwrite warning");
  assert.ok(res.warnings.some(w => /could not fetch/.test(w)), "expected fetch-failure warning");
  // Artifacts unchanged.
  assert.equal(await readFile(join(repoRoot, "catalog/builtins.generated.yaml"), "utf8"), sentinel);
  assert.equal(await readFile(join(repoRoot, "catalog/agents.generated.yaml"), "utf8"), sentinel);
  assert.equal(await readFile(join(repoRoot, "NOTICE"), "utf8"), "PRE-EXISTING NOTICE\n");
});

